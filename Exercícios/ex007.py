#Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.

notap = float(input('Digite a sua nota da matéria de português: '))
notam = float(input('Digite a sua nota da matéria de matematica: '))
print(f'A média entre as suas notas de português e matemática é {(notap+notam)/2:.0f}.')